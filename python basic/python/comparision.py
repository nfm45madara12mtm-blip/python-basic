#comparision operators

a= 15
b = 5

# 1. Equal to (==)
print(a==b)
print(b==5)
   

# 2. Not equal to (!=)
print(a!=b)
print(b!=5)

# 3. Greater than (>)
print(a>b)
print(b>a)

# 4. Less than (<)
print(a<b)
print(b<a)
       
# 5. Greater than or equal to (>=)
print(a>=b)
print(b>=a)
  

# 6. Less than or equal to (<=)
print(a<=b)
print(b<=a)
